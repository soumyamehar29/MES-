ORG 0000H

MOV A, 50H
MOV R0, A

MOV A, 51H
MOV R1, A

CLR A

COMPARE:
MOV A, R0
JZ CHECK_B_ZERO

MOV A, R1
JZ A_GREATER

DEC R0
DEC R1
SJMP COMPARE

CHECK_B_ZERO:
MOV A, R1
JZ EQUAL
SJMP B_GREATER

A_GREATER:
MOV 52H, #01H
SJMP END_PROGRAM

EQUAL:
MOV 52H, #00H
SJMP END_PROGRAM

B_GREATER:
MOV 52H, 30FFH

END_PROGRAM:
END
ORG 0000H

MOV R0, #40H
MOV R1, #40H

SCAN_LOOP:
MOV A, @R0

CJNE A, #0FFH, VALID_DATA

SJMP NEXT_BYTE

VALID_DATA:
MOV @R1, A
INC R1

NEXT_BYTE:
INC R0
CJNE R0, #60H, SCAN_LOOP

FILL_ZERO:
CJNE R1, #60H, FILL_CONT
SJMP END_PROGRAM

FILL_CONT:
MOV A, #00H
MOV @R1, A
INC R1
SJMP FILL_ZERO

END_PROGRAM:
END